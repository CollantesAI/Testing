# standard arithmatic operators
print(10 + 3)  # addition
print(10 - 3)  # subtraction
print(10 * 3)  # multiplication
print(10 / 3)  # division (float)
print(10 // 3)  # division (integer)
print(10 % 3)  # modulo
print(10 ** 3)  # exponent

# augmented assignment operators (works with all operators)
x = 10
x = x + 3  # manual increment of 10 + 3
x += 3  # assigment equivalent of 10 + 3
