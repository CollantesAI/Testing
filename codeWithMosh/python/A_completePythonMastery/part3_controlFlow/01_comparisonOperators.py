# comparison operators
print("Comparison Operators:")
print("10 > 3:", 10 > 3)
print("10 < 20:", 10 < 20)
print("10 <= 3:", 10 <= 3)
print("10 == 10:", 10 == 10)
print("10 == \"10\":", 10 == "10")
print("10 != \"10\":", 10 != "10")


# comparison operators with strings
# follows lexicographical order
print("\nComparison Operators With Strings")
print("\"bag\" > \"apple\":", "bag" > "apple")
print("\"bag\" == \"BAG\":", "bag" == "BAG")
